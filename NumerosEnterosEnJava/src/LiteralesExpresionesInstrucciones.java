public class LiteralesExpresionesInstrucciones {
    public static void main(String[] args) {
        int primerNumero = 5;
        int segundoNumero = 3;
        int tercerNumero = segundoNumero;
        System.out.println(tercerNumero);
        int suma = 6 + 4;
        int resta = 10 - 5;
        System.out.println(suma);
        System.out.println(resta);
        int producto = 4 * 4;
        int cociente = 12 / 3;
        System.out.println(producto);
        System.out.println(cociente);
        int cociente2 = 18 / 5;
        System.out.println(cociente2);
        int resultado = 10 % 3;
        System.out.println(resultado);
        int resultadoComplejo = (primerNumero + 5) * (segundoNumero + tercerNumero);
        System.out.println(resultadoComplejo);
    }
}
